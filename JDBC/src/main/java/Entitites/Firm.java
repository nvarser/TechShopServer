package Entitites;

public class Firm {
    private int id_firm;
    private String firm_name;

    public int getId_firm() {
        return id_firm;
    }

    public void setId_firm(int id_firm) {
        this.id_firm = id_firm;
    }

    public String getFirm_name() {
        return firm_name;
    }

    public void setFirm_name(String firm_name) {
        this.firm_name = firm_name;
    }
}
